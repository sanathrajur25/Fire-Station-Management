            <h1>
               <p>FIRE PREVENTION, FIRE FIGHTING AND FIRE CONTROL:-
INTRODCTION ABOUT THE FIRE SAFETY:-
 A safe and secure environment is a prerequisite for effective teaching and learning. Thus
ensuring life safety of students, professors, teaching staffs and staff members during disasters
is very necessary. In the light of recent tragedies involving school children, like the
Kumbakonam fire tragedy, Dabwali fire incident and earthquakes around the world where
school children were affected due to unsafe school/college/institute buildings. It becomes of
utmost importance that safety of student is given due consideration. The requirement for fire
prevention, life safety in relation to fire and fire protection of buildings, Occupancy and
protection features that are necessary to minimize danger to life and property from fire. Fire
prevention measures are a key element in the fire safety management of institute. This involves
the identification and elimination of potential fire hazards both inside and outside the building,
and the establishment of good housekeeping practices, periodic inspections and the diligent
application of safety rules. To ensuring safety of staff and student in hostel and in institute, the
Security and Fire Safety wing has been taken a lot of fire safety measures.<br>
<br>
FIRE: - A fire occurs when the elements i.e. heat, fuel, oxygen and chemical chain reaction are
present and combined in the right mixture. A fire can be prevented or extinguished by removing
any one of the elements in the fire tetrahedron. Essentially all four elements must be present for
fire to occur, heat, fuel oxygen, and a chemical chain reaction.
CLASSES OF FIRE:
 Class A - fires involving solid materials such as wood, paper or textiles.
 Class B - fires involving flammable liquids such as petrol, diesel or oils.
 Class C - fires involving gases.
 Class D - fires involving metals.
 Class E - fires involving live electrical apparatus. (Technically ‘Class E’ doesn’t exists
however this is used for convenience here)
 Class F - fires involving cooking oils such as in deep-fat fryers.
<br><br>
CHARACTERISTICS OF FIRE:
In order to protect yourself from fire, it is important to understand the basic characteristics of
fires. A fire has many characteristics and some of them are listed below:
 A fire can occur at any time.
 Short circuit is one of the leading causes of fire.
 In just two minutes, a residence can be engulfed in flames.
 The water is the best medium to fight fires except electrical and oil fires.
 Most deaths due to fire occur at night when people are sleeping.
 Fire produces gases that make you drowsy.
 Smoke and poisonous gases are the primary killer in fires.
 Instead of being awakened by fire, you may fall into a deeper sleep.
 Asphyxiation is the leading cause of fire deaths exceeding burns.
 Heat and smoke from fire can be more dangerous than the flames.
 Inhaling the super hot air can sear your lungs.
 Pouring water on electrical or oil fires will be dangerous.
<br><br><br>
POTENTIAL FIRE HAZARDS:
The best measures to be adopted for the prevention of a fire are to eliminate potential fire
hazards. Therefore you need to know what fire hazards are and what you should do to remove
them from your home or workplace. Some potential fire hazards are listed below:
 Electric wiring in poor condition.
 Electric system those are overloaded, resulting in hot wiring or connections, or failed
components.
 Storage of flammable liquids.
 Storage of combustibles with insufficient protection.
 Storage of combustibles near equipment that generate heat, flame or sparks.
 Smoking of cigarettes, cigars, pipes, beedees, etc.
 Ignition sources such as candles, lighters, match, etc.
 Equipment that generate heat and utilizes combustibles.
 Use of cooking appliances, stoves, furnaces, boilers, heaters, ovens, etc. disregarding
safety guidelines.
 Poor housekeeping practices.
<br><br><br>
FIRE PREVENTION:
<li>
A fire can occur at any time. Therefore various measures are to be adopted in advance to
prevent a fire in your building. Some of the measures need to be adopted are given below:
<br>
 Prohibit smoking in storage areas of flammable materials.
<br>
 If electrical equipment is not working properly or if it gives off an unusual odour
disconnect the equipment and call the duty electrician.
<br>
 Properly replace any electrical cord that is cracked or has broken connection.
<br>
 When using extension cords, protect them from damage. Do not put them across
doorways or any place where they will be stepped on or chafed. Check the amperage
load specified by the manufacturer.
<br>
 Do not plug an extension cord into another, and do not plug more than one extension
cord into one outlet.
<br>
 Keeps all heat producing appliances away from the wall and away from anything that
might burn and spread fire. Leave plenty of space for air to circulate around equipment
that normally gives off heat.
<br>
 Make sure all appliances in your area such as hot plates, ovens, toasters, mixers,
grinders, geezers, clothing irons are turned off when not in use.
<br>
 Use ash trays and empty them only when you are sure the ashes, matches and butts are
cold.
<br>
 Make sure that no one including visitors has left cigarettes smoldering in waste –
baskets or on furniture’s, sofas, beds, etc.
<br>
 Keep storage areas, stairway landings and other out of way locations free of waste
paper, empty cartons, dirty rags and other material that could fuel a fire.
<br>
 Report all fire hazards to the institute security & fire safety wing.
<br>
 Create awareness to use fire retardant furniture’s, carpets, curtains, etc.
<br>
 Follow good housekeeping practices – because a clean house is a safe house.
Fire Safety Precautions
<br>
 Don't overload outlets with multiple outlet cords or plugs. If additional outlets are
required, use an IS marked multi-outlet "power strip" with its own built-in circuit breaker.
<br>
 Careless smoking, use of candles and incense, and unattended cooking appliances
remain major causes of fatal fires in residential buildings. Use of fire crackers, hot work,
and open fire is prohibited in all buildings.
<br>
 Keep walkways, stairwells and exits free from obstructions at all times.
<br>
 Report immediately if you observe/noticed damaged fire equipment and other fire
hazards to Campus security and Fire Safety wing.
<br>
 Do not open fire hydrant/hose reel water for unnecessarily use.
<br>
 Do not misuse of Fire Extinguisher.
<br>
<li>
<br><br><br><br>
 KITCHEN FIRE: - Thousands of fire incidents occur every year in which many residential
structures are also involved. Majority of the fire incidents, in residential houses, have actually
emanated from the kitchen, while cooking food, which had been left unattended, on the stove.
<br>
 Never leave your cooking unattended. The cooking vessel could overflow and extinguish
the burners, causing gas to leak.
<br>
 Never get distracted. If you are called away, turn off the gas.
<br>
 Keep the flame from extending past the pot side -Turn pot handles away from the stove
edge.
<br>
 Be especially careful when cooking with oil or fat. Fats and cooking oils will ignite once
they have reached a certain temperature.
<br>
 Never use water to put out fat and oil fires. Water can cause a fire to spread rapidly and
inflict horrific burns.
<br>
 Ensure good ventilation in your kitchen by keeping the windows open.
<br>
 Never allow a child to cook without adult supervision.
<br>
 Wear tight-fitting sleeves when cooking.
<br>
 Do not place flammable or plastic items near the flame.
<br>
 Close the regulator knob to OFF position when the cylinder is not in use.
<br>
 Do not bring and accumulate cylinders in kitchen( Hostel and canteen) concerned
authorities please instruct to respective mess and canteen contractor/worker.
<br><br><br><br>
</li>
<li>
In Case of a Leakage: 
<br><br>
 Do not panic and calm your mind.
<br>
 Do not turn on or turn off any electrical appliances in the kitchen if you think that there is
a leak.
<br>
 Put out all flames, lights, incense sticks etc.
<br>
 Shut down the appliance and turn off the LPG regulator.
<br>
 Immediately put on the Safety Cap on the cylinder after the regulator is switched off.
<br>
 Keep all the windows and doors open to ensure ventilation. But do not switch on electric
fans or exhaust fans for this purpose.
<br>
 Get in touch with your dealer at the earliest.
<br>
<br><br>
</li>
Gas Bottle & Cylinder Fire
Sound the alarm and call the fire fighting services.
Evacuate all personnel, except those necessary to deal with the emergency, from the danger
area (particularly if in path of any cloud).

<br><br><br>
<li>
In case of a leak with fire
<br><br>
 A small fire from a bottle may be smothered with a wet cloth or dry powder extinguisher,
ONLY if it is possible to stop the leak.
<br>
 Cool with water any adjacent cylinder, which cannot be moved to a safe place.
<br>
 Always approach any fire or leak from upwind and using all protection available.
<br><br>
</li>

    <br><br>
It is best to control gas fires and not to extinguish them until the sources are cut off. Due regard
should be taken of the possibility of exploding bottles and jets of flame from relief valves.
Responding To Fire
Heat and toxic smoke from fire build up with surprising speed, quickly blocking escape paths.
Few people are burned to death in fires; most die from smoke inhalation. Taking fire alarms
seriously and exiting buildings quickly are essential to your survival.
When fire is discovered:
<br><br>
<li>
• Activate the nearest fire alarm (if installed)
• Notify the local Fire Department by calling: Fire Control Room Raipur:-101
• If the fire alarm is not available, notify the site personnel about the fire
emergency by the following means: By shouting Fire, Fire, Fire.
In the case of a small fire:
    <br><br>
 Notify others nearby; call 101
<br>
 If it is safe to do so, use a fire extinguisher
<br>
 If the fire is still burning, get out
<br>
<br>
In the case of a large fire or smoke:
    <br>
 Notify others; call 101
<br>
 Activate fire alarm
<br>
 Leave building quickly via the stairs
<br>
If there are injuries, call 108
<br>
Fight the fire ONLY if :
•The Fire Department has been notified.
•The fire is small and is not spreading to other areas.
•Escaping the area is possible by backing up to the nearest exit.
•The fire extinguisher is in working condition and personnel are trained to use it.
Upon being notified about the fire emergency, occupants must:
•Leave the building using the designated escape routes.
•Assemble in a safe open area
•Remain outside until the competent authority (Authority) announces that it is safe to
reenter.
Designated Official, Emergency Coordinator or supervisors must
•Disconnect utilities and equipment unless doing so jeopardizes his/her safety.
•Coordinate an orderly evacuation of personnel.
•Perform an accurate head count of personnel reported to the designated area.
•Determine a rescue method to locate missing personnel.
•Provide the Fire Department personnel with the necessary information about the facility.
Area/Floor Monitors must:
•Ensure that all employees have evacuated the area/floor.
•Report any problems to the Emergency Coordinator at the assembly area.
Assistants to Physically Challenge should:
•Assist all physically challenged students/visitors/staff/employees in emergency evacuation.
What to do in a Fire
STEP 1
Don’t panic! Take two seconds to think. You’re going to be scared, but you need to stay calm to
get out alive.
STEP 2
Feel the wall / door with the back of your hand. If very hot don’t go out! There is a fire behind it.
If you CAN’T leave the room:
Consider lowering yourself out of the window.
 You should survive a 1 floor jump onto tar/concrete. Anything higher is questionable.
 Ideally throw a mattress out first to land on. Don’t launch yourself out of the window, but
hang down by your arms before dropping to the ground to minimize your fall. Bend your
knees when you land.
 Do not break the window until you’re about to jump as you can’t stop smoke coming in
afterwards.
If a window jump is impossible:
 Fill the bath/basin with water and use dampened bed sheets, towels or clothes wedged
in door cracks to stop smoke entering. Wet the walls and doors. No water? Pee on them!
 Then signal to rescuers from the window using a torch or a white sheet.
If you CAN leave the room:
 Take the room key with you if it’s to hand. You may need it to rush back in.
 Smoke rises and so will be high, filling down to the floor. Keep low or better still crawl
where the oxygen is.
 Stay close to the walls to avoid panicking guests and to count doors to the fire exit.
 Do not use lift/elevator - that’s an oven you don’t want to be trapped in!
 Do not re-enter under any circumstances until told it is safe by the fire brigade. (Better to
lose a backpack than your life).
The most Important Rule:-Is the exit corridor filled with smoke?
DON’T try and cover your mouth and run through it unless you can guarantee a maximum five
second clear run to the outside (if you have walked the route when you checked in you will know
if you can make it or not).
If the smoke gets in your eyes they will shut and not open again. If you get trapped the smoke
will then kill you, so don’t try and beat it. Head back to your room
If A Fire Starts In Your Room, Office, Etc.
 Leave the room and close the door behind you to keep smoke and flames out of the hall.
 Sound the fire alarm by activating the nearest pull station, and leave the building by the
closest exit or by raising alarm by shouting fire, fire, fire.
 Call Campus security and fire Safety wing from a safe location.
If You Hear a Fire Alarm
 Go to the door of your room and feel the door with your hand.
 If the door or the knob is hot, leave it shut. (See next section: "If the room door is hot...")
 Check the hall. If you can leave safely, take your keys with you, close the door behind
you and go to the nearest clear exit. Use an alternate route if your path is blocked at any
point.
 Do not use the elevator. You could be trapped or let out into a fire area.
If The Room Door Is Hot, or You Are Forced Back To Your Room by Smoke
This is the choice of last resort. Make every effort to leave the building at the first alarm or other
evidence of fire.
 Let someone know you are in the room. If the phone works, call Campus Security and
Safety wing.
 If your window can be opened, hang a bed sheet or similar item out the window to signal
the rescue team, but close the window against smoke if necessary.
Should I Try To Put Out The Fire?
Fight a fire only if it is small and you believe you can put it out without risking your safety.
If the fire is small and:
 an extinguisher is readily available,
 you are familiar with its operation,
 you can fight the fire without blocking your exit path
 The extinguisher is compatible with what's burning (e.g., flammable liquids or live electric
equipment, see below) Then attempt to extinguish the fire.
SAFETY TIP: The first priority in responding to a fire is preservation of life. No one is obliged to
fight a fire.
Fire Extinguisher Operation
 Remove the fire extinguisher from its supporting bracket carefully; extinguishers are
surprisingly heavy. The lower handle on the valve will support the extinguisher when
carried.
 Remove the pin from the handle by pulling the ring, breaking the plastic tamper-evident
seal.
 Aim the nozzle at the base of the flames, squeeze the handles together, and sweep the
nozzle slowly from side to side, across the width of the flames until the fire is
extinguished or the extinguisher is empty. You may repeatedly start and stop the flow of
the extinguisher by squeezing and releasing the top handle.
 If a fire is not successfully controlled with one extinguisher, you should leave
immediately.
Inform to the Campus security and Safety wing, even if you successfully extinguish the fire.
Compatibility of Extinguishers and Fires
Dry chemical Powder (DCP) extinguishers are safe and effective against all ordinary types of
fires).
Pressurized water extinguishers are effective only against ordinary combustibles, such as
paper, wood, fabric, trash, etc. They must never be used on flammable liquid/oil fires or fires
involving live electrical circuits.
Carbon dioxide extinguishers shaped black nozzle, no pressure gauge work only against
flammable liquid fires and are safe to use around live electrical circuits. They will not extinguish
fires involving ordinary combustibles and must be discharged within about 3 feet of flames to be
effective.
Helping a Person with Clothing or Hair on Fire
"Stop, Drop and Roll":.
 You must immediately get the person flat on the ground. Do not allow her or him to run.
 Extinguish the flames by rolling the person on the ground. A jacket or blanket may be
used to help smother the flames if immediately available.
 Seconds count. Do not waste time looking for an extinguisher or water source.
 Douse the person with water as soon thereafter as possible. Do not attempt to remove
burned clothing.
 Call Campus security and fire Safety wing</p> 
               
